# 整数：加减乘除
print(1 + 2)
print(1 * 2)
print(1 - 2)
print(1 / 2)
print(2 / 3)

# 两个个乘号表示乘方
print(2 ** 3)
# 三个以上乘号没有这种，报错
# print(2 *** 3)

# 支持次序运算
print(2 * (3 + 1))

# 浮点数：带小数点的数字都是浮点数
# 需要注意浮点数运算表示不精确问题
print(0.1 + 0.2)
print(0.1 - 0.2)
print(0.1 * 0.2)
print(0.1 / 0.2)

# 字符拼接数字
# print("小明今年" + 7 + "岁") 会报错，因为无法识别7是作为字符的，直接数字运算，所以报错
print("小明今年" + str(7) + "岁")
print("小明今年" + str(3 + 5) + "岁")